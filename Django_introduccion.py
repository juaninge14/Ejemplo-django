# Django 

# Instalacion local e instalacion virtual
#   Instalacion local
#     Nos permite trabajar con una unica version de Django
#     Nos permite trabajar con una unica version de Python
#     Se implementan las mismas dependencias para todos los proyectos 

#   Instalacion virtual
#     Nos permite trabajar con diversas veriones de Django
#     Nos permite trabajar con diversas versiones de Python
#     Se implementan diversas dependencias para los proyectos 
#     En los cuales se pueden implementar entornos de desarrollo, pruebas y producciones de codigo

#  Ejemplo instalacion local --> version Django 2.3 con version de Python 3.7
#  Ejemplo instalacion virtual -->  versiones de Django 3.6 y 2.2 con versiones de Python 2.9 y 3.8


